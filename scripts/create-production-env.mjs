#!/usr/bin/env node
import { randomBytes } from "node:crypto";
import { access, writeFile } from "node:fs/promises";
import path from "node:path";
import { pathToFileURL } from "node:url";

const DEFAULT_OUTPUT = ".env.production";
const DEFAULT_VERIFIER_ID = "agent:private-beta-verifier";

export function buildProductionEnv(options = {}) {
  const origin = normalizeHttpsOrigin(requiredOption(options.origin, "--origin"));
  const email = requiredOption(options.email, "--email");
  if (!email.includes("@")) throw new Error("--email must be an email address");
  const target = normalizeTarget(options.target || "compose");

  const verifierId = options.verifier || DEFAULT_VERIFIER_ID;
  if (!String(verifierId).startsWith("agent:")) {
    throw new Error("--verifier must start with agent:");
  }

  const randomToken = options.randomToken || secureToken;
  const backupRemoteHost = String(options.backupRemoteHost || "").trim();
  const backupRemoteDir = backupRemoteHost ? "/data/backup-remote" : "";
  const humanName = options.name || email.split("@")[0] || "Admin";
  const publicPort = String(options.publicPort || "4173");
  const identityEnv = [
    "MFA_WORKSPACE_ID=workspace:default",
    "MFA_WORKSPACE_NAME=math-for-agents",
    "MFA_HUMAN_ID=human:admin",
    `MFA_HUMAN_EMAIL=${email}`,
    `MFA_HUMAN_NAME=${quoteEnvValue(humanName)}`,
    `MFA_HUMAN_PASSWORD=${randomToken(32)}`,
    `MFA_HUMAN_KEY=mfa_${randomToken(32)}`,
    `MFA_DEFAULT_VERIFIER_AGENT_ID=${verifierId}`,
    "MFA_DEFAULT_VERIFIER_NAME=Private beta verifier",
    ""
  ];
  const webEnv = [
    "ARTIFACT_MAX_BYTES=10000000",
    "MAX_JSON_BYTES=",
    "MFA_COOKIE_SECURE=true",
    "MFA_ALLOW_INSECURE_COOKIES=false",
    `MFA_PUBLIC_ORIGIN=${origin}`,
    `MFA_BASE_URL=${origin}`,
    "MFA_TRUST_PROXY=true",
    ""
  ];
  const rateLimitEnv = [
    "MFA_RATE_LIMIT_ENABLED=true",
    "MFA_RATE_LIMIT_WINDOW_MS=60000",
    "MFA_RATE_LIMIT_LOGIN_MAX=10",
    "MFA_RATE_LIMIT_WRITE_MAX=120",
    "MFA_RATE_LIMIT_READ_MAX=600",
    ""
  ];
  const logEnv = [
    "MFA_LOG_REQUESTS=true",
    "MFA_LOG_ERRORS=true",
    "MFA_LOG_ERROR_STACKS=false",
    ""
  ];
  const healthcheckEnv = [
    "MFA_HEALTHCHECK_TIMEOUT_MS=5000",
    "MFA_HEALTHCHECK_BEARER=",
    "MFA_HEALTHCHECK_ASSIGNMENTS=false",
    ""
  ];

  if (target === "vercel") {
    return [
      "# Generated by: npm run env:production -- --target vercel --origin <https-origin> --email <owner-email>",
      "# Do not commit this file. Add these values to Vercel project environment variables.",
      "",
      "MFA_DEPLOY_TARGET=vercel",
      `DATABASE_URL=${options.databaseUrl || ""}`,
      "DATABASE_SSL=true",
      "ARTIFACT_STORAGE_DRIVER=vercel-blob",
      `BLOB_READ_WRITE_TOKEN=${options.blobReadWriteToken || ""}`,
      "",
      ...identityEnv,
      ...webEnv,
      ...rateLimitEnv,
      ...logEnv,
      ...healthcheckEnv,
      "MFA_WORKER_RUNNER=disabled",
      "MFA_WORKER_POLL_MS=5000",
      "MFA_WORKER_TIMEOUT_MS=60000",
      "MFA_WORKER_MAX_OUTPUT_BYTES=256000",
      "",
      "BACKUP_REMOTE_DIR=",
      ""
    ].join("\n");
  }

  return [
    "# Generated by: npm run env:production -- --origin <https-origin> --email <owner-email>",
    "# Do not commit this file. It contains private beta secrets.",
    "",
    "POSTGRES_USER=math_for_agents",
    "POSTGRES_DB=math_for_agents",
    `POSTGRES_PASSWORD=${randomToken(32)}`,
    "",
    ...identityEnv,
    "ARTIFACT_STORAGE_DRIVER=local-file",
    ...webEnv,
    "HOST=0.0.0.0",
    "PORT=4173",
    `MFA_PUBLIC_PORT=${publicPort}`,
    "",
    "MFA_WORKER_RUNNER=docker",
    "MFA_WORKER_POLL_MS=5000",
    "MFA_WORKER_TIMEOUT_MS=60000",
    "MFA_WORKER_MAX_OUTPUT_BYTES=256000",
    "MFA_WORKER_IMAGE=python:3.12-alpine",
    "MFA_WORKER_DOCKER_CPUS=1",
    "MFA_WORKER_DOCKER_MEMORY=512m",
    "",
    ...rateLimitEnv,
    ...logEnv,
    "BACKUP_DIR_HOST=./backups",
    `BACKUP_REMOTE_DIR_HOST=${backupRemoteHost}`,
    `BACKUP_REMOTE_DIR=${backupRemoteDir}`,
    "",
    ...healthcheckEnv
  ].join("\n");
}

export async function writeProductionEnvFile(options = {}) {
  const output = path.resolve(options.cwd || process.cwd(), options.output || DEFAULT_OUTPUT);
  if (!options.force && await exists(output)) {
    throw new Error(`${path.basename(output)} already exists; pass --force to overwrite`);
  }
  const text = buildProductionEnv(options);
  await writeFile(output, text, { mode: 0o600 });
  return { output, text };
}

export function parseArgs(argv) {
  const options = {};
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (arg === "--help" || arg === "-h") {
      options.help = true;
      continue;
    }
    if (arg === "--force") {
      options.force = true;
      continue;
    }
    const key = arg.startsWith("--") ? arg.slice(2) : "";
    if (!key) throw new Error(`unknown argument: ${arg}`);
    const value = argv[index + 1];
    if (!value || value.startsWith("--")) throw new Error(`${arg} requires a value`);
    index += 1;
    if (key === "origin") options.origin = value;
    else if (key === "email") options.email = value;
    else if (key === "name") options.name = value;
    else if (key === "target") options.target = value;
    else if (key === "database-url") options.databaseUrl = value;
    else if (key === "blob-read-write-token") options.blobReadWriteToken = value;
    else if (key === "output") options.output = value;
    else if (key === "verifier") options.verifier = value;
    else if (key === "backup-remote-host") options.backupRemoteHost = value;
    else if (key === "public-port") options.publicPort = value;
    else throw new Error(`unknown argument: ${arg}`);
  }
  return options;
}

function usage() {
  return `Usage:
  npm run env:production -- --origin https://math-for-agents.example.com --email you@example.com
  npm run env:production -- --target vercel --origin https://math-for-agents.example.com --email you@example.com

Options:
  --origin <https-origin>          Public browser/API origin, no path
  --email <owner-email>           First human owner email
  --target <compose|vercel>        Env shape to generate, default compose
  --database-url <postgres-url>    Vercel target only; hosted Postgres URL
  --blob-read-write-token <token>  Vercel target only; private Blob token
  --name <owner-name>             First human owner display name
  --output <path>                 Output file, default .env.production
  --verifier <agent:id>           Default verifier agent id
  --backup-remote-host <path>     Mounted off-host backup path on the VM
  --public-port <port>            Host port exposed by Compose, default 4173
  --force                         Overwrite the output file
`;
}

function requiredOption(value, name) {
  const normalized = String(value || "").trim();
  if (!normalized) throw new Error(`${name} is required`);
  return normalized;
}

function normalizeHttpsOrigin(value) {
  let url;
  try {
    url = new URL(value);
  } catch {
    throw new Error("--origin must be a valid HTTPS origin");
  }
  if (url.protocol !== "https:") throw new Error("--origin must use https://");
  if (["localhost", "127.0.0.1", "::1"].includes(url.hostname)) {
    throw new Error("--origin must not point at localhost");
  }
  if (url.pathname !== "/" || url.search || url.hash) {
    throw new Error("--origin must not include a path, query, or hash");
  }
  return url.origin;
}

function normalizeTarget(value) {
  const target = String(value || "").trim().toLowerCase();
  if (target !== "compose" && target !== "vercel") {
    throw new Error("--target must be compose or vercel");
  }
  return target;
}

function quoteEnvValue(value) {
  if (/^[A-Za-z0-9_.:@/-]+$/.test(String(value))) return String(value);
  return `"${String(value).replace(/\\/g, "\\\\").replace(/"/g, '\\"')}"`;
}

function secureToken(bytes) {
  return randomBytes(bytes).toString("base64url");
}

async function exists(filePath) {
  try {
    await access(filePath);
    return true;
  } catch {
    return false;
  }
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  try {
    const options = parseArgs(process.argv.slice(2));
    if (options.help) {
      console.log(usage());
    } else {
      writeProductionEnvFile(options)
        .then(({ output }) => {
          console.log(`wrote ${path.relative(process.cwd(), output) || output}`);
          console.log("next: npm run preflight:deploy -- .env.production");
        })
        .catch((error) => {
          console.error(error.message);
          process.exitCode = 1;
        });
    }
  } catch (error) {
    console.error(error.message);
    process.exitCode = 1;
  }
}
